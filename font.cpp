void fillMemoryWithFont()
{
  for(int i = 0; i < 128; i++)
  {
    for(int j = 0; j < 8; j++)
    {
      int byte1 = font[i][(j*2)];
      int byte2 = font[i][(j*2) + 1];

      memVarType[font_start_address + 256 + (i*8) + j] = 0;
      memory[font_start_address + 256 + (i*8) + j].value.i = (byte1 << 16) | byte2;
    }
  }
}

